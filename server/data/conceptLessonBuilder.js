function pickCoreFormula(content) {
  if (!content?.formula) return '';
  const parts = String(content.formula)
    .split('|')
    .map((s) => s.trim())
    .filter(Boolean);
  return parts[0] || content.formula;
}

function normalizeFigureSvg(svg) {
  if (svg == null || typeof svg !== 'string') return null;
  const t = svg.trim();
  if (!t) return null;
  return t.replace(/%23/gi, '#');
}

